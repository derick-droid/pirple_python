import math
# square root
value = 9
srtval = math.sqrt(value)
print(srtval)
# exponential
expoval = math.exp(value)
print(expoval)
# factorial of a value
factvalue = math.factorial(value)
print(factvalue)
# sin of a value
sinvalue = math.sin(value)
print(sinvalue)

# floor of a value
floorvalue = math.floor(value)
print(floorvalue)
# ceiling of a value
ceilvalue = math.ceil(value)

print(ceilvalue)
